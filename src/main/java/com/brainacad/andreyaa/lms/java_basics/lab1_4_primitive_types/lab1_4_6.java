package com.brainacad.andreyaa.lms.java_basics.lab1_4_primitive_types;

public class lab1_4_6 {
    public static void main(String[] args) {

        int a = 10;
        int b = 12;

        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(~a);
        System.out.println(~b);

    }
}
