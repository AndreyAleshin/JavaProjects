package com.brainacad.andreyaa.lms.java_basics.lab1_4_primitive_types;

public class lab1_4_5 {
    public static void main(String[] args) {

        int a = 4;
        int b = 4;

        System.out.println(a << b * 2);
        System.out.println(a << b / 2);
        System.out.println(a >> b * 2);
        System.out.println(a >> b / 2);
        System.out.println(a >>> b * 2);
        System.out.println(a >>> b / 2);

    }
}
